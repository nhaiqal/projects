#include	<unistd.h>

int	main(int ac, char **av)
{
	int	i;

	i = ac;
	if (i < 2)
		write (1, "\n", 1);
	else
	{
		while (*av[i - 1])
		{
			write (1, av[i - 1], 1);
			av[i - 1]++;
		}
		write (1, "\n", 1);
	}
	return (0);
}
