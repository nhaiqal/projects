#include	<unistd.h>

int	main(int ac, char **av)
{
	int		i;
	char	l;

	if (ac != 2)
		write (1, "\n", 1);
	else
	{
		i = 0;
		while (av[1][i])
		{
			l = av[1][i];
			if (av[1][i] >= 'a' && av[1][i] <= 'z')
				l -= 32;
			else if (av[1][i] >= 'A' && av[1][i] <= 'Z')
				l += 32;
			write (1, &l, 1);
			i++;
		}
		write (1, "\n", 1);
		return (0);
	}
}
