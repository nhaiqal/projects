#include	<unistd.h>

void	ft_putchar(char c)
{
	write (1, &c, 1);
}

void	ft_putnbr(int nb)
{
	long	i;

	i = nb;
	if (i < 0)
	{
		ft_putchar('-');
		i = -i;
	}
	if (i / 10 != 0)
	{
	ft_putnbr(i / 10);
	}
	ft_putchar('0' + (i % 10));
}
